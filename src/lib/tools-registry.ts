function createFileTool(tool: Omit<ToolDefinition, "mode">): ToolDefinition {
  return {
    ...tool,
    mode: "file",
  };
}

function createFormTool(
  tool: Omit<ToolDefinition, "mode" | "fileAccept" | "allowsMultipleFiles" | "dropHint">
): ToolDefinition {
  return {
    ...tool,
    mode: "form",
  };
}

