export type ToolAccent = "red" | "blue" | "green" | "purple" | "orange";
export type ToolCategory =
  | "Organize"
  | "Optimize"
  | "Convert to PDF"
  | "Convert from PDF"
  | "Edit"
  | "Security"
  | "Intelligence"
  | "Developer Tools"
  | "Financial Tools";
export type ToolInputType = "number" | "text" | "select" | "password" | "textarea";
export type ToolMode = "file" | "form";
export type ToolProvider = "local" | "cloudconvert" | "gemini" | "hybrid";

export interface ToolInput {
  name: string;
  label: string;
  type: ToolInputType;
  placeholder?: string;
  options?: string[];
  helpText?: string;
  step?: string;
  min?: number;
  required?: boolean;
}

export interface ToolDefinition {
  id: string;
  title: string;
  category: ToolCategory;
  description: string;
  seoTitle?: string;
  seoDescription?: string;
  iconName: string;
  accent: ToolAccent;
  featured?: boolean;
  buttonLabel: string;
  mode: ToolMode;
  provider: ToolProvider;
  resultPreview?: "json-tree";
  fileAccept?: string;
  allowsMultipleFiles?: boolean;
  dropHint?: string;
  inputs: ToolInput[];
  keywords?: string[];
}

